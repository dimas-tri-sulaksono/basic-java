package Day1.Method;

public class MultipleMethod {
    // method instance : method yang dimiliki sebuah object/class
    public static int multiple(int num1, int num2){return num1 * num2;}

    /* method overload : bikin method dengan name yang sama tapi dengan hasil tipe yang data dan param yang beda */
    public static int multiple(int num1, int num2, int num3){return num1 * num2 * num3;}

    // multiple double
//    public static int multipleDouble(double num1, double num2, double num3){return num1 * num2 * num3;}

}
